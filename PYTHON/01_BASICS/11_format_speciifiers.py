#format specifiers = are used to control how values are displayed when formatting output.
#They are most commonly used with f-strings and the format()

price1 = 3.14159
price2 = -987.65
price3 = 12.34

print(f"Price 1 is K{price1:.2f}")
print(f"Price 2 is K{price2:.4f}")
print(f"Price 3 is K{price3:.2f}")